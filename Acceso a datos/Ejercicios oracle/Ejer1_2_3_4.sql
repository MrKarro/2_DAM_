CREATE OR REPLACE TYPE DIRECCION AS OBJECT (
CALLE VARCHAR2(25),
CIUDAD VARCHAR2(29),
COD_POSTAL NUMBER(5),

MEMBER PROCEDURE SET_CALLE(C VARCHAR2),
MEMBER FUNCTION GET_CALLE RETURN VARCHAR2,
MEMBER PROCEDURE SET_CIUDAD(C VARCHAR2),
MEMBER FUNCTION GET_CIUDAD RETURN VARCHAR2,
MEMBER PROCEDURE SET_COD_POSTAL(C NUMBER),
MEMBER FUNCTION GET_COD_POSTAL RETURN NUMBER);
/

CREATE OR REPLACE TYPE BODY DIRECCION AS
    MEMBER PROCEDURE SET_CALLE(C VARCHAR2) IS
    BEGIN
        CALLE := C;
    END SET_CALLE;

    MEMBER FUNCTION GET_CALLE RETURN VARCHAR2 IS
    BEGIN
        RETURN CALLE;
    END GET_CALLE;

    MEMBER PROCEDURE SET_CIUDAD(C VARCHAR2) IS
    BEGIN
        CIUDAD := C;
    END SET_CIUDAD;

    MEMBER FUNCTION GET_CIUDAD RETURN VARCHAR2 IS
    BEGIN
        RETURN CIUDAD;
    END GET_CIUDAD;

    MEMBER PROCEDURE SET_COD_POSTAL(C NUMBER) IS
    BEGIN
        COD_POSTAL := C;
    END SET_COD_POSTAL;

    MEMBER FUNCTION GET_COD_POSTAL RETURN NUMBER IS
    BEGIN
        RETURN COD_POSTAL;
    END GET_COD_POSTAL;
END;
/

