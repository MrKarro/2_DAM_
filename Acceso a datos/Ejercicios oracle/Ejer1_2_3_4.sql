CREATE OR REPLACE TYPE ALUMNOS AS OBJECT (
COD NUMBER,
PERS PERSONA,
PRIMERA NUMBER(2),
SEGUNDA NUMBER(2),
TERCERA NUMBER(2) ,

MEMBER PROCEDURE SET_COD(C NUMBER),
MEMBER FUNCTION GET_COD RETURN NUMBER,
MEMBER PROCEDURE SET_PERS(C PERSONA),
MEMBER FUNCTION GET_PERS RETURN PERSONA,
MEMBER PROCEDURE SET_PRIMERA(C NUMBER),
MEMBER FUNCTION GET_PRIMERA RETURN NUMBER,
MEMBER PROCEDURE SET_SEGUNDA(C NUMBER),
MEMBER FUNCTION GET_SEGUNDA RETURN NUMBER,
MEMBER PROCEDURE SET_TERCERA(C NUMBER),
MEMBER FUNCTION GET_TERCERA RETURN NUMBER ,
MEMBER FUNCTION GET_MEDIA RETURN NUMBER);

/
CREATE OR REPLACE TYPE BODY ALUMNOS AS

    MEMBER PROCEDURE SET_COD(C NUMBER) IS
    BEGIN
        COD := C;
    END SET_COD;

    MEMBER FUNCTION GET_COD RETURN NUMBER IS
    BEGIN
        RETURN COD;
    END GET_COD;

    MEMBER PROCEDURE SET_PERS(C PERSONA) IS
    BEGIN
        PERS := C;
    END SET_PERS;

    MEMBER FUNCTION GET_PERS RETURN PERSONA IS
    BEGIN
        RETURN PERS;
    END GET_PERS;

    MEMBER PROCEDURE SET_PRIMERA(C NUMBER) IS
    BEGIN
        PRIMERA := C;
    END SET_PRIMERA;

    MEMBER FUNCTION GET_PRIMERA RETURN NUMBER IS
    BEGIN
        RETURN PRIMERA;
    END GET_PRIMERA;

    MEMBER PROCEDURE SET_SEGUNDA(C NUMBER) IS
    BEGIN
        SEGUNDA := C;
    END SET_SEGUNDA;

    MEMBER FUNCTION GET_SEGUNDA RETURN NUMBER IS
    BEGIN
        RETURN SEGUNDA;
    END GET_SEGUNDA;

    MEMBER PROCEDURE SET_TERCERA(C NUMBER) IS
    BEGIN
        TERCERA := C;
    END SET_TERCERA;

    MEMBER FUNCTION GET_TERCERA RETURN NUMBER IS
    BEGIN
        RETURN TERCERA;
    END GET_TERCERA;
    MEMBER FUNCTION GET_MEDIA RETURN NUMBER IS
    BEGIN
        RETURN (PRIMERA + SEGUNDA + TERCERA) / 3;
    END GET_MEDIA;
END;
