public class Centro {
    String denom;
    String ciudad;
    
    Centro(String denom, String ciudad){
        this.denom = denom;
        this.ciudad = ciudad;
    }

    public String getDenom() {
        return denom;
    }

    public void setDenom(String denom) {
        this.denom = denom;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }
    
    
}
