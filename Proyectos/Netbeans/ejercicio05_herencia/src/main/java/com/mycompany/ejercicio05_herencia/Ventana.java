    private void jButtonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMostrarActionPerformed
        if (!comprobarCampos()){
            JOptionPane.showMessageDialog(this, "Tiene algún campo sin completar.");
            return;
            
        }
        try{
            if (jRadioButtonEmpleado.isSelected()){

                int codigo = Integer.parseInt(jTextFieldCodigo.getText());
                String cargo = jTextFieldCargo.getText();
                double sueldo = Double.parseDouble(jTextFieldSueldo.getText());
                int id = Integer.parseInt(jTextFieldId.getText());
                String nombre = jTextFieldNombre.getText();
                String dni = jTextFieldDni.getText();
                String domicilio = jTextFieldDomicilio.getText();
                String telefono = jTextFieldTelefono.getText();
                Empleado emp = new Empleado(codigo, cargo, sueldo, id, nombre, dni, domicilio, telefono);
                modelo.addElement(emp.toString());
                jList1.setModel(modelo);
            }
            if (jRadioButtonJubilado.isSelected()){
                int edad = Integer.parseInt(jTextFieldEdad.getText());
                String ocupacion = jTextFieldOcupacion.getText();
                double sueldo = Double.parseDouble(jTextFieldSueldoJubi.getText());
                int id = Integer.parseInt(jTextFieldId.getText());
                String nombre = jTextFieldNombre.getText();
                String dni = jTextFieldDni.getText();
                String domicilio = jTextFieldDomicilio.getText();
                String telefono = jTextFieldTelefono.getText();
                Jubilado jub = new Jubilado(sueldo, ocupacion, edad, id, nombre, dni, domicilio, telefono);
                modelo.addElement(jub.toString());
                jList1.setModel(modelo);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Ha habido algún error de entrada de datos.");
        }
        
        
    }//GEN-LAST:event_jButtonMostrarActionPerformed

