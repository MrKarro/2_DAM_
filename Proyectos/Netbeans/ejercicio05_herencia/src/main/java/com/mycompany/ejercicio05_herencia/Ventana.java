    private boolean comprobarCampos() {
        boolean llenos = true;
        if (jTextFieldId.getText().isEmpty())
            llenos = false;
        if (jTextFieldNombre.getText().isEmpty())
            llenos = false;
        if (jTextFieldDni.getText().isEmpty())
            llenos = false;
        if (jTextFieldDomicilio.getText().isEmpty())
            llenos = false;
        if (jTextFieldTelefono.getText().isEmpty())
            llenos = false;
        
        if (jRadioButtonEmpleado.isSelected()){
            if (jTextFieldCodigo.getText().isEmpty())
                llenos = false;
            if (jTextFieldSueldo.getText().isEmpty())
            llenos = false;
            if (jTextFieldCargo.getText().isEmpty())
            llenos = false;
        }
        if (jRadioButtonJubilado.isSelected()){
            if (jTextFieldEdad.getText().isEmpty())
                llenos = false;
            if (jTextFieldSueldoJubi.getText().isEmpty())
            llenos = false;
            if (jTextFieldOcupacion.getText().isEmpty())
            llenos = false;
        }
        
        return llenos;
        
    }
