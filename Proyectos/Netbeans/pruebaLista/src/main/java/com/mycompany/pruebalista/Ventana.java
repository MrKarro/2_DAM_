    private void jButtonAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnadirActionPerformed
        Alumno nuevo = new Alumno (jTextFieldNombre.getText(), jTextFieldApellidos.getText(), jComboBoxCurso.getSelectedItem().toString());
        
        
        modelo.addElement(nuevo.toString());
        jList1.setModel(modelo);
        jTextFieldNombre.setText("");
        jTextFieldApellidos.setText("");
        jComboBoxCurso.setSelectedIndex(0);
        
        
    }//GEN-LAST:event_jButtonAnadirActionPerformed

    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
        modelo.remove(jList1.getSelectedIndex());
        jList1.setModel(modelo);
    }//GEN-LAST:event_jButtonBorrarActionPerformed

    private void jButtonLlenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLlenarActionPerformed
        modelo.clear();
        for (int i = 0; i < NOMBRES_DEFAULT.length; i++)
            modelo.addElement(NOMBRES_DEFAULT[i].toString());
        jList1.setModel(modelo);
    }//GEN-LAST:event_jButtonLlenarActionPerformed

    private void jButtonLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLimpiarActionPerformed
        modelo.clear();
        jList1.setModel(modelo);
    }//GEN-LAST:event_jButtonLimpiarActionPerformed

    private void jButtonPasarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPasarActionPerformed
        // TODO add your handling code here:
        String pasar = "";
        for (int i = 0; i < (int) jList1.getModel().getSize(); i++){
            pasar += jList1.getModel().getElementAt(i).toString() + "\n";
        }
        jTextArea1.setText(pasar);
        
    }//GEN-LAST:event_jButtonPasarActionPerformed

