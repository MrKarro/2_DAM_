    private void jComboBox_coloresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox_coloresActionPerformed
        // TODO add your handling code here:
        int pos = jComboBox_colores.getSelectedIndex();
        switch (pos){
            case 0:
                jTextField_salida.setBackground(java.awt.Color.RED);
                break;
            case 1:
                jTextField_salida.setBackground(java.awt.Color.YELLOW);
                break;
            case 2:
                jTextField_salida.setBackground(java.awt.Color.GREEN);
                break;
            case 3:
                jTextField_salida.setBackground(java.awt.Color.BLACK);
                break;
            
        }
    }//GEN-LAST:event_jComboBox_coloresActionPerformed

