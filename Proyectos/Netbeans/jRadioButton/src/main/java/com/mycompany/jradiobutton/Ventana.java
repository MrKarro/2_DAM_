    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jRadioButton_rojo.isSelected()){
            jTextField_salida.setBackground(java.awt.Color.RED);
        }
        else if (jRadioButton_amarillo.isSelected()){
            jTextField_salida.setBackground(java.awt.Color.YELLOW);
        }
        else if (jRadioButton_verde.isSelected()){
            jTextField_salida.setBackground(java.awt.Color.GREEN);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

